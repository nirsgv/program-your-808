  constructor({ instruments, notes }) {
    this.part = instruments.reduce((acc, inst) => {
      acc[inst] = Array.from({ length: 16 }).map((item, index) =>
        notes[inst] && Array.isArray(notes[inst]) && notes[inst].includes(index)
          ? 1
          : 0
      );
      return acc;
    }, {});
    this.part.uid = uuidv4();
  }
